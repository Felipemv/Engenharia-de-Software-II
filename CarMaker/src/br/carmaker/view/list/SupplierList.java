    @Override
    public Component getListCellRendererComponent(JList<? extends JSupplier> list,
            JSupplier value, int index, boolean isSelected, boolean hasFocus) {

        String name = "<html><strong>" + JConstants.NAME + ": </strong>" + value.getName() + "</html>";
        String address = "<html><strong>" + JConstants.ADDRESS + ": </strong>" + value.getAddress() + "</html>";
        String cnpj = "<html><strong>" + JConstants.CNPJ + ": </strong>" + value.getCnpj() + "</html>";
        String leadTime = "<html><strong>" + JConstants.LEAD_TIME + ": </strong>" + Integer.toString(value.getLeadTime()) + "</html>";
        
        lblName.setText(name);
        lblAddress.setText(address);
        lblCnpj.setText(cnpj);
        lblLeadTime.setText(leadTime);

        if (isSelected) {
            jPanel1.setBackground(Color.yellow);
        } else {
            jPanel1.setBackground(Color.white);
        }

        return this;
    }


