    @Override
    public Component getListCellRendererComponent(JList<? extends JRecievedOrder> list, 
            JRecievedOrder value, int index, boolean isSelected, boolean hasFocus) {
        
        
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
        
        lblProtocol.setText(value.getProtocol());
        lblCar.setText(value.getCar().getModel());
        lblShippingCompany.setText(value.getCar().getColor());
        lblDealership.setText(Double.toString(value.getCar().getSalePrice()));
        lblStatus.setText(value.getStatus().toString());
        lblDate.setText(sdf.format(value.getExpectedDate()));
        
        if(isSelected){
            jPanel2.setBackground(Color.yellow);
        }else{
            jPanel2.setBackground(Color.white);
        }
        
        return this;
    }
